package pacman.core;

/***
 * Enum representing the direction of movement.
 */
public enum Direction {
	/**
	 * Represents that an object is moving upwards.
	 */
	UP,
	
	/**
	 * Represents that an object is moving to the right.
	 */
	RIGHT,
	
	/**
	 * Represents that an object is moving downwards.
	 */
	DOWN,
	
	/**
	 * Represents that an object is moving to the left.
	 */
	LEFT,
	
	/**
	 * Represents that an object does not move in either direction.
	 */
	NONE;
}
